namespace MudTools.OfficeInterop;

/// <summary>
/// 指定文档主题颜色索引的枚举类型，用于标识Word文档中使用的不同主题颜色
/// </summary>
public enum WdThemeColorIndex
{
    /// <summary>
    /// 表示非主题颜色，值为-1
    /// </summary>
    wdNotThemeColor = -1,

    /// <summary>
    /// 主题深色1
    /// </summary>
    wdThemeColorMainDark1,

    /// <summary>
    /// 主题浅色1
    /// </summary>
    wdThemeColorMainLight1,

    /// <summary>
    /// 主题深色2
    /// </summary>
    wdThemeColorMainDark2,

    /// <summary>
    /// 主题浅色2
    /// </summary>
    wdThemeColorMainLight2,

    /// <summary>
    /// 强调颜色1
    /// </summary>
    wdThemeColorAccent1,

    /// <summary>
    /// 强调颜色2
    /// </summary>
    wdThemeColorAccent2,

    /// <summary>
    /// 强调颜色3
    /// </summary>
    wdThemeColorAccent3,

    /// <summary>
    /// 强调颜色4
    /// </summary>
    wdThemeColorAccent4,

    /// <summary>
    /// 强调颜色5
    /// </summary>
    wdThemeColorAccent5,

    /// <summary>
    /// 强调颜色6
    /// </summary>
    wdThemeColorAccent6,

    /// <summary>
    /// 超链接颜色
    /// </summary>
    wdThemeColorHyperlink,

    /// <summary>
    /// 已访问的超链接颜色
    /// </summary>
    wdThemeColorHyperlinkFollowed,

    /// <summary>
    /// 背景颜色1
    /// </summary>
    wdThemeColorBackground1,

    /// <summary>
    /// 文本颜色1
    /// </summary>
    wdThemeColorText1,

    /// <summary>
    /// 背景颜色2
    /// </summary>
    wdThemeColorBackground2,

    /// <summary>
    /// 文本颜色2
    /// </summary>
    wdThemeColorText2
}