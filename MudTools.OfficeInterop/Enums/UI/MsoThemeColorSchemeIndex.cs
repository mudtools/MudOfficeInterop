namespace MudTools.OfficeInterop;

/// <summary>
/// 指示 Office 主题的颜色方案
/// </summary>
public enum MsoThemeColorSchemeIndex
{
    /// <summary>
    /// 指定深色方案 1
    /// </summary>
    msoThemeDark1 = 1,

    /// <summary>
    /// 指定浅色方案 1
    /// </summary>
    msoThemeLight1,

    /// <summary>
    /// 指定深色方案 2
    /// </summary>
    msoThemeDark2,

    /// <summary>
    /// 指定浅色方案 2
    /// </summary>
    msoThemeLight2,

    /// <summary>
    /// 指定强调色 1
    /// </summary>
    msoThemeAccent1,

    /// <summary>
    /// 指定强调色 2
    /// </summary>
    msoThemeAccent2,

    /// <summary>
    /// 指定强调色 3
    /// </summary>
    msoThemeAccent3,

    /// <summary>
    /// 指定强调色 4
    /// </summary>
    msoThemeAccent4,

    /// <summary>
    /// 指定强调色 5
    /// </summary>
    msoThemeAccent5,

    /// <summary>
    /// 指定强调色 6
    /// </summary>
    msoThemeAccent6,

    /// <summary>
    /// 指定超链接的颜色方案
    /// </summary>
    msoThemeHyperlink,

    /// <summary>
    /// 指定已访问超链接的颜色方案
    /// </summary>
    msoThemeFollowedHyperlink
}